<h1>Sobre</h1>
<p>Laravel é um framework PHP livre e open-source criado por Taylor B.
 Otwell para o desenvolvimento de sistemas web que utilizam o padrão MVC.</p>
 <b>
 Uma aplicação web tem como função essencial receber requisições de um cliente, tratá-las <br>
 enviando alguma respostas de volta, geralmente utilizando o HTTP como protocolo de comunicação.
No Laravel, essas funções são desempenhadas por componentes que seguem a arquitetura MVC, <br>
model-view-controller. Isso significa que para criar uma aplicação web com esse framework <br>
precisamos definir as rotas, endereços para os recursos, bem como seus controllers e as views <br>
com as quais o usuário vai interagir.
 </b>
